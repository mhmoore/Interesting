    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return Coordinators.interest.allInterests.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: InterestsTableViewCell.reuseId, for: indexPath) as? InterestsTableViewCell else { return UITableViewCell() }

        let interest = Coordinators.interest.allInterests[indexPath.row]
        cell.configure(with: interest)

        return cell
    }

